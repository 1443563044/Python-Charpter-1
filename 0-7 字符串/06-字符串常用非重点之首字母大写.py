mystr = 'hello world and itcast and itheima and Python'

# 1.capitalize() 字符首字母大写,后面会变成小写
new_str = mystr.capitalize()
print(f'1', new_str)

# 2.title()  每个单词首字母都大写
new_str2 = mystr.title()
print('2', new_str2)

# 3.upper()  把小写转大写
new_str3 = mystr.upper()
print('3', new_str3)

# 4.lower()  把大写转换成小写
new_str4 = mystr.lower()
print('4', new_str4)

